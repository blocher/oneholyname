function webfonts_render() {
	global $webfonts;
	$fontsref='';
	$font_options = array(
						get_option('diary_font_title'),
						get_option('diary_font_date'),
						get_option('diary_font_more'),
						get_option('diary_font_heading'),
						get_option('diary_font_sideheading')
					);
	foreach($font_options as $font_option){
		if($font_option!=''){
			foreach($webfonts as $webfont){
						if($webfont['name'] == $font_option){ 
							$variant = $webfont['variant'];
								}	
					}
					if(!strstr($fontsref, $font_option)){
						if($variant){
							$fontsref .= $font_option.":".$variant."|";
							}else{
							$fontsref .= $font_option."|";
							}
					}
			}
			}
	$fontsref = str_replace(" ","+",$fontsref);	
	$output = '<link href="http://fonts.googleapis.com/css?family=' . $fontsref .'" rel="stylesheet" type="text/css" />'."\n\n";
	$output = str_replace('|"','"',$output);
	echo $output;
}

